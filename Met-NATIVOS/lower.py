# Como funciona o lower()
# O método lower() em Python é usado para converter todos os caracteres de uma string para minúsculas.
# Isso é útil quando você deseja fazer comparações de strings sem se preocupar com diferenças de maiúsculas e minúsculas.
# Exemplo:
nome = "JUVENALDO"
print(nome.lower())  # Saída: "hello world"
