# Como converter a primeira letra de uma string para maiúscula
# O método capitalize() em Python é usado para converter a primeira letra de uma string para maiúscula e todas as outras letras para minúscula.
# Isso é útil quando você deseja formatar nomes ou títulos corretamente.
# Exemplo:
nome = "juvenaldo"
print(nome.capitalize())  # Saída: "Juvenaldo"
