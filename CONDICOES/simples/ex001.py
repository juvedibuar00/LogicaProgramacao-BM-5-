# O que é uma condicional simples?
# É uma estrutura de controle que executa um bloco de código apenas se uma condição for verdadeira.
# Sintaxe:
# if condição:
#     bloco de código
# Exemplo:
idade = 18
if idade >= 18:
    print("Você é maior de idade.")
# Se a condição idade >= 18 for verdadeira, a mensagem será exibida.
# Caso contrário, nada acontecerá.
