# O que é uma condicional compsta?

# É uma estrutura de controle que executa um bloco de código se uma condição for verdadeira e outro bloco se for falsa.
# Sintaxe:
# if condição:
#     bloco de código se verdadeira
# else:
#     bloco de código se falsa
# Exemplo:
idade = 18
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")